export function generateWindowsScript(product, version) {
  const key = WINDOWS_KMS_KEYS[product]?.[version] || '';
  if (!key || !WINDOWS_PRODUCTS[product] || !WINDOWS_VERSIONS[product]?.[version]) return '';
  

  const kmsServer = import.meta.env.VITE_KMS_SERVER_ADDRESS || 's1.kms.cx';
  const script = `slmgr /skms ${kmsServer}
slmgr /ipk ${key}
slmgr /ato
slmgr /xpr
pause`;
  
  return script;
}